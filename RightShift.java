class RightShift
{
  public static void main(String args[])
        {
             System.out.println(10>>2);  //10/2^2=10/4=2
             System.out.println(20>>2);  //20/2^2=10/4=2
             System.out.println(30>>2);  //30/2^2=10/4=2
             System.out.println(-34>>3);
             System.out.println(-34>>>3);
        }
}
